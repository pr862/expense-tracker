// Helper function to get icon component by category value
export const getCategoryIcon = (categoryValue, size = 20) => {
  const category = categoryIcons[categoryValue];
  if (category) {
    return <category.icon size={size} color={category.color} />;
  }
  return <OtherIcon size={size} color="#6B7280" />;
};

